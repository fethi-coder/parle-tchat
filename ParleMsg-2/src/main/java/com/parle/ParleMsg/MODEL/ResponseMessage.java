package com.parle.ParleMsg.MODEL;

public class ResponseMessage {

	
	private String mesage;
	
	
	

	public ResponseMessage() {
		super();
	}


	public ResponseMessage(String mesage) {
		super();
		this.mesage = mesage;
	}


	public String getMesage() {
		return mesage;
	}

	public void setMesage(String mesage) {
		this.mesage = mesage;
	}
	
}
